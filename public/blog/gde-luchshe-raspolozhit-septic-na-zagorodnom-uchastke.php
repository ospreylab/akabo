        <h1>Где лучше располодить септик на загородном участке?</h1>
        <div class="post-img post-5"></div>
        <div class="post-text">
            <p>
                Речь пойдет о выборе наилучшего места под установку станции биологической очистки стоков.
                Это немаловажная составляющая при планировании размещения объектов на загородном участке.
                Правильное расположение ЛОС позволит избежать дорогостоящих переделок в будущем.
                Существует ряд правил, которые следует соблюсти.
            </p>

            <h2>1. Осуществить установку станции как можно ближе к месту сброса очищенной воды</h2>
            <p>
                В случае, когда длина трубы принудительного выброса со станции более 4-х метров, потребуется установка греющего кабеля.
                Он, как и любой электрический прибор, может выйти из строя, что приведет к серьезным проблемам в холодное время года.
                Также греющий кабель расходует электроэнергию (около 15 Вт на погонный метр), увеличивая счет за электричество.
                Если есть возможность, то лучше избежать необходимости его установки.
            </p>

            <h2>2. Расположить станцию не далее 10 метров от ближайшего подъезда грузовой машины</h2>
            <p>
                Многие современные станции при обслуживании не требуют участия ассенизаторской машины, но бывают случаи (неправильная эксплуатация), при которых откачка потребуется.
                Станции с осадкой твердых частиц в приемной камере обслуживаются только при помощи ассенизатора.
                Именно поэтому всегда нужно предусмотреть место подъезда.
                Стандартная длина шланга у такой машины — 15 метров. Дополнительные шланги, как правило, оплачиваются отдельно.
            </p>

            <h2>3. Не монтировать Лос под окнами спален и кухни</h2>
            <p>
                Любая станция является источником звука.
                Гул от компрессора и шум от переливов эрлифтов может мешать сну при открытых окнах.
                Также при неправильной эксплуатации может появиться неприятный запах.
            </p>

            <h2>4. Не ставить септик на парковке и заезде машин</h2>
            <p>
                Ни в коем случае не следует монтировать станцию в месте проезда и парковки автомобилей.
                Наезд на горловину повлечет дорогой ремонт пластикового корпуса.
                В зимнее время года, если уборка снега с парковки осуществляется с помощью трактора, есть большая вероятность повреждения горловины.
            </p>

            <h2>5. Не ставить станцию в низком месте</h2>
            <p>
                В глинистых грунтах при отсутствии дренажной системы на поверхности скапливается вода.
                Если поставить септик в низине, то его может затопить талыми водами или сильными осадками.
                Обязательно нужно учитывать особенности грунтов и вероятности затопления.
                Недопустимо попадание внешних вод в очистное сооружение, а их попадание в компрессорный отсек приведет к серьезным поломкам оборудования.
            <p>
            <p>
                Существует СНиП для правильного расположения септика с расстояниями от дома, забора и источников водоснабжения, которому также необходимо следовать.
                Выбор места в любом случае остается за хозяином участка, но соблюдение вышеперечисленных правил поможет избежать переделок в будущем.
            </p>
