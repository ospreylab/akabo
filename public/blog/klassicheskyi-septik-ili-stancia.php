        <h1>Классический септик или станция?</h1>
        <div class="post-img post-6"></div>
        <div class="post-text">
            <p>
                Перед человеком, строящим загородный дом, непременно встает вопрос: какую систему очистки сточных вод установить на своем участке? 
                Существует множество различных емкостей для решения этой задачи, но все они делятся на два типа: переливной классический септик и станция биологической очистки (не берем в расчет накопительные емкости). 
                В данной статье речь пойдет о плюсах и минусах этих вариантов.
            </p>    
            <h2>Септик.</h2>
            <p>Классический переливной септик — это рабочее решение для очистки сточных вод на 60 %. 
               Это может быть готовое изделие из пластика, разделенное на несколько камер. 
               Также септик можно сделать из бетонных колец. 
               Работать данная система будет только в фильтрующих почвах и при низком уровне грунтовых вод.
            </p>
            <h3>Плюсы:</h3>
            <p>
                + Энергонезависимость.
            </p>    
            <p>
                Такая система не требует электроэнергии. Работает круглый год без вмешательства человека.
            </p>    
            <p>
                + Устойчивость к засорам.
            </p>
            <p>
                Качество сточных вод не имеет значения. 
                Попадание влажных салфеток и прочей не разлагаемой продукции не повлияет на работу септика.
            </p>
            <h3>Минусы:</h3>
            <p>
                - Низкая степень очистки.
            </p>
            <p>
                Качество очистки воды остается низким, на выходе получается мутная вода, имеющая гнилостный запах.
                Такую воду запрещено отводить напрямую в открытую канаву.
            </p>
            <p>
                - Запах.
            </p>
            <p>
                Септики выделяют неприятный запах, поэтому желательно устанавливать отводящую вентиляцию на фасад дома.
            </p>
            <h2>Станция биологической очистки.</h2>
            <p>
                На рынке существует огромное множество изготовителей станций биологической очистки.
                Это инженерное сооружение с электрооборудованием, способное очищать сток на 97 %. 
                Эта система может быть установлена в любых грунтах.
            </p>
            <h3>Плюсы:</h3>
             <p>
                + Высокая степень очистки.
            </p>
            <p>
                Очищенную воду со станции можно сливать открытым способом в канаву.
            </p>
            <p>
                + Отсутствие запахов.
            </p>
            <p>
                При правильной работе станции не может возникать неприятных запахов.
            </p>
            <p>
                + Экологичность.
            </p>
            <p>
                Сток от станции не загрязняет окружающую среду.
            </p>
            <h3>Минусы:</h3>
            <p>
                - Энергозависимость.
            </p>
            <p>
                Станция требует постоянного подключения к электричеству и постоянного надзора со стороны пользователя.
            </p>
            <p>
                - Определенные требования к качеству сточных вод.
            </p>
            <p>
                Есть ряд ограничений при использовании канализации. 
                При нарушении определенных правил можно засорить станцию или уничтожить колонию бактерий в ней.
            </p>
             <p>
                Большинство современных домовладельцев отдают предпочтение станциям биологической очистки, не смотря на все трудности в процессе эксплуатации, но при подходящих условиях классический септик также имеет право на существование.
            </p>
        </div>
