        <h1>Рекомендации по эксплуатации станции биологической очистки</h1>
        <div class="post-img post-3"></div>
        <div class="post-text">
            <p>
                Как и любое технологическое оборудование, станция биологической очистки требует внимания в процессе эксплуатации. Чтобы избежать проблем и дорогостоящего ремонта необходимо соблюдать ряд правил при использовании очистного сооружения.
            </p>

            <h2>1. Визуальный осмотр каждые две-три недели</h2>
            <p>
                Важно открывать крышку и проводить осмотр электрооборудования на предмет поломок.
            </p>
            <p>
                Вышедший из строя насос принудительного выброса может привести к затоплению всего компрессорного отсека. На этот случай на станциях предусмотрена аварийная сигнализация, но т. к. эта вещь устанавливается опционально и есть не на всех устройствах, пользователь может вовремя не узнать о поломке. Часто бывает, что и сама аварийная сигнализация может выйти из строя, например из-за перегоревшей сигнальной лампы.
            </p>
            <p>
                Переполненная приемная камера говорит о засоре главного эрлифта или вышедшем из строя компрессоре.
            </p>
            <p>
                Наличие плотного ила во вторичном отстойнике свидетельствует о засоре дегазатора.
            </p>
            <p>
                В случае обнаружения комков жира, влажных салфеток, средств гигиены в приемной камере, следует их удалить. Для этого можно использовать сачок.
            </p>
            <p>
                Все вышеперечисленные проблемы следует устранить в кратчайшие сроки, чтобы избежать более серьезных последствий.
            </p>

            <h2>2. Прочистка фильтра компрессора раз в 2-3 месяца</h2>
            <p>
                Компрессор через специальные отверстия в крышке или горловине станции втягивает воздух через фильтр. Пыль с участка и пыльца с растений оседают на нем, уменьшая количество подачи воздуха. В какой-то момент, если не следить за чистотой фильтра, уровень загрязнения может стать критическим, и компрессор перегреется и сгорит. Поэтому очень важно соблюдать эту рекомендацию в процессе эксплуатации септика.
            </p>

            <h2>3. Строгое соблюдение правил эксплуатации, заявленных производителем оборудования</h2>
            <p>
                Любая станция биологической очистки требовательна к качеству стока в канализацию.
            </p>
            <p>
                Запрещается сливать в станцию:
            </p>
            <ul>
                <li>строительный мусор (известь, остатки плиточного клея, и т.д.)</li>
                <li>остатки пищи и воду от промывания грибов</li>
                <li>пыль из пылесоса</li>
                <li>шерсть животных</li>
            </ul>
            <p>
                Ни в коем случае нельзя использовать хлорсодержащие моющие средства. Хлор погубит сообщество микроорганизмов активного ила и станция перестанет полноценно работать.
            </p>
            <p>
                Важно не сливать объем воды больший, чем станция способна переработать за сутки, т. к. это может привести к вымыванию активного ила.
            </p>
            <p>
                Вышеперечисленные рекомендации помогут поддерживать корректную работу септика, и также важно помнить, что все станции биологической очистки требуют регулярного сервисного обслуживания.
            </p>
        </div>
